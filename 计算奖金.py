x = float(input("输入利润[单位:万元]"))
jj = 0
if x < 10 :
    jj = x*0.1
elif x<20 :
    jj = (x-10)*0.075 + 10 * 0.1
elif x<40 :
    jj = (x-20)*0.05 + (x-10)*0.075 + 10 * 0.1
elif x<60 :
    jj = (x-40)*0.03 + (x-20)*0.05 + (x-10)*0.075 + 10 * 0.1
elif x<100 :
    jj = (x-60)*0.015 + (x-40)*0.03 + (x-20)*0.05 + (x-10)*0.075 + 10 * 0.1
else:
    jj = (x-100)*0.01 + (x-60)*0.015 + (x-40)*0.03 + (x-20)*0.05 + (x-10)*0.075 + 10 * 0.1
print("奖金为:{:.2f}万元".format(jj))